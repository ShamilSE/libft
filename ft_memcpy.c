#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char *ptr;

	ptr = dest;
	if (!src && !dest)
		return (NULL);
	while (n--)
		*(unsigned char *)dest++ = *(unsigned char *)src++;
	return (ptr);
}
