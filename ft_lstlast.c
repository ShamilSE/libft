#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*last;
	t_list	*p;

	last = lst;
	p = last;
	while (last != NULL)
	{
		p = last;
		last = last->next;
	}
	return (p);
}
