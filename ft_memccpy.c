#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (n--)
	{
		if (((unsigned char *)src)[i] != (unsigned char)c)
			*(unsigned char *)dst++ = ((unsigned char *)src)[i++];
		else
		{
			*(unsigned char *)dst++ = ((unsigned char *)src)[i];
			return (dst);
		}
	}
	return (NULL);
}
