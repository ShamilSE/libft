#include "libft.h"

void	*ft_memchr(const void *arr, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	sym;

	ptr = (unsigned char *)arr;
	sym = (unsigned char)c;
	while (n--)
	{
		if (*ptr == sym)
			return ((void *)ptr);
		ptr++;
	}
	return (NULL);
}
